"""
词表总表

设计目标：
1. 不改现有管线依赖的常量名；
2. 把零散词表整理成更清晰的层次；
3. 为后续对象归一化、目标明确性、可行动性等规则升级预留基础。
"""


def _flatten_term_groups(term_groups: dict[str, list[str]]) -> list[str]:
    seen: set[str] = set()
    merged: list[str] = []
    for terms in term_groups.values():
        for term in terms:
            if term not in seen:
                seen.add(term)
                merged.append(term)
    return merged


